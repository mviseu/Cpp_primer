#include <iostream>

using std::cin;
using std::cout;
using std::endl;

int main() {
	int i, j, k;
	cin >> i >> j >> k;

	// For j > k this will be true if i is different from 0
	// For j < k this will be true if i is different from 1
	if(i != j < k) {
		cout << "True" << endl;
	} else {
		cout << "False" << endl;
	}



	return 0;
}